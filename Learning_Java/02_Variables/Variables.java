class Variables {
    
    static int age = 18; // A variable that belongs to the class itself.

    public static void main(String[] args) {

        int age = 23; // A variable that belongs to the main method.

        System.out.println("I am " + age + " years old.");
    }
    
}

/*
 * In naming variables:
 * No number
 * No space
 * No reserved words
*/